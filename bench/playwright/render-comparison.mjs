#!/usr/bin/env node
// Join heso-compat-suite JSON with playwright sidecar JSON into a
// side-by-side markdown comparison. Targets that exist in only one
// tool's output are still listed; their missing column is blank.
//
//   node render-comparison.mjs heso-results.json playwright-results.json > RESULTS.md
//
// Reads both files relative to its working dir. Exit 2 on bad input.

import { readFileSync } from 'node:fs';
import { resolve } from 'node:path';

function die(msg) {
    console.error(msg);
    process.exit(2);
}

const [, , hesoPath, pwPath] = process.argv;
if (!hesoPath || !pwPath) {
    die('usage: node render-comparison.mjs HESO.json PLAYWRIGHT.json');
}

const heso = JSON.parse(readFileSync(resolve(hesoPath), 'utf8'));
const pw = JSON.parse(readFileSync(resolve(pwPath), 'utf8'));

// Index both by name for the join. Same-name in both inputs is the
// hit; anything in one alone shows as a blank in the other column.
const byName = new Map();
for (const r of heso.results) {
    byName.set(r.name, { name: r.name, category: r.category, heso: r, pw: null });
}
for (const r of pw.results) {
    if (byName.has(r.name)) {
        byName.get(r.name).pw = r;
    } else {
        byName.set(r.name, { name: r.name, category: r.category, heso: null, pw: r });
    }
}

const rows = [...byName.values()];

function fmt(n) {
    return n == null ? '—' : String(n);
}
function speedup(hesoMs, pwMs) {
    if (hesoMs == null || pwMs == null || hesoMs === 0) return '—';
    return (pwMs / hesoMs).toFixed(2) + '×';
}

// Aggregates over targets where BOTH tools have a number — that's the
// only honest comparison.
const matched = rows.filter(r => r.heso && r.pw);
const hesoSum = matched.reduce((a, r) => a + r.heso.ms_total, 0);
const pwSum = matched.reduce((a, r) => a + r.pw.ms_total, 0);
const hesoMean = matched.length ? Math.round(hesoSum / matched.length) : 0;
const pwMean = matched.length ? Math.round(pwSum / matched.length) : 0;

const out = [];
out.push('# heso vs Playwright — head-to-head');
out.push('');
out.push(`Generated by \`node render-comparison.mjs\` joining `
    + `\`${hesoPath}\` and \`${pwPath}\`. `
    + `${matched.length} of ${rows.length} targets exist in both tools' outputs `
    + '(the Playwright sidecar runs only the 8 non-SPA targets by default).');
out.push('');
out.push('Same URL, same probe, same network. Wall-clock per target ('
    + `total = navigate + parse + extract value):`);
out.push('');
out.push('| Target | Category | heso ms | Playwright ms | Playwright / heso |');
out.push('|---|---|---:|---:|---:|');
for (const r of rows) {
    const hMs = r.heso?.ms_total;
    const pMs = r.pw?.ms_total;
    out.push(`| ${r.name} | ${r.category} | ${fmt(hMs)} | ${fmt(pMs)} | ${speedup(hMs, pMs)} |`);
}
out.push('');
out.push(`**Mean of matched targets**: heso **${hesoMean} ms**, `
    + `Playwright **${pwMean} ms** → Playwright is `
    + `**${(pwSum / hesoSum).toFixed(2)}×** as slow on this workload.`);
out.push('');
out.push('## Caveats');
out.push('');
out.push('- Playwright cold-starts Chromium per invocation of the sidecar (one process for all targets).');
out.push('  heso is a one-shot binary; the suite reuses a single `reqwest` client across targets. '
    + 'Apples-to-apples for "extract title from N URLs in one run."');
out.push('- httpbin.org is occasionally slower on heso because Playwright sometimes hits a warmer keep-alive.');
out.push('  Run the suite a few times if you want a tighter spread.');
out.push('- These numbers are total wall-clock including the network. Pure engine cost '
    + '(what scales when you swap in cassettes) is the `ms_eval` column in each JSON.');
out.push('');

process.stdout.write(out.join('\n'));
